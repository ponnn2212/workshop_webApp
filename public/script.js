// Function เพื่อตรวจสอบอักขระพิเศษในชื่อ (ใช้ได้ทั้งชื่อห้องและชื่อผู้เล่น)
// อนุญาตเฉพาะตัวอักษรไทย, อังกฤษ (ทั้งเล็กและใหญ่), ตัวเลข
function isValidName(name) {
    // Regex: ^[ก-๙a-zA-Z0-9]+$
    // ^      : เริ่มต้นสตริง
    // [ก-๙] : ตัวอักษรไทยทั้งหมด
    // a-zA-Z : ตัวอักษรภาษาอังกฤษ
    // 0-9    : ตัวเลข
    // +      : มีอย่างน้อยหนึ่งตัว
    // $      : จบสตริง
    const regex = /^[ก-๙a-zA-Z0-9]+$/;
    return regex.test(name);
}